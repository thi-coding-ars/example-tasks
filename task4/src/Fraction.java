public class Fraction {
    int numerator;
    int denominator;

    void setNumerator(int value) {
        this.numerator = value;
    }

    void setDenominator(int value) {
        this.denominator = value;
    }
    
    void print() {
        System.out.println(this.numerator + "/" + this.denominator);
    }
}
